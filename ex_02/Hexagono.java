package poligonos;

public class Hexagono extends PoligonosRegulares {

    private float ladoA;
    private float ladoB;
    private float ladoC;
    private float ladoD;
    private float ladoE;
    private float ladoF;

    public Hexagono(float ladoA, float ladoB, float ladoC, float ladoD, float ladoE, float ladoF) {
        this.ladoA = ladoA;
        this.ladoB = ladoB;
        this.ladoC = ladoC;
        this.ladoD = ladoD;
        this.ladoE = ladoE;
        this.ladoF = ladoF;
    }
    
    

    public float getLadoA() {
        return ladoA;
    }

    public void setLadoA(float ladoA) {
        this.ladoA = ladoA;
    }

    public float getLadoB() {
        return ladoB;
    }

    public void setLadoB(float ladoB) {
        this.ladoB = ladoB;
    }

    public float getLadoC() {
        return ladoC;
    }

    public void setLadoC(float ladoC) {
        this.ladoC = ladoC;
    }

    public float getLadoD() {
        return ladoD;
    }

    public void setLadoD(float ladoD) {
        this.ladoD = ladoD;
    }

    public float getLadoE() {
        return ladoE;
    }

    public void setLadoE(float ladoE) {
        this.ladoE = ladoE;
    }

    public float getLadoF() {
        return ladoF;
    }

    public void setLadoF(float ladoF) {
        this.ladoF = ladoF;
    }


    
    
}
